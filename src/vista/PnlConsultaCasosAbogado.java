    private void cmbAbogadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbAbogadosActionPerformed
        int pos = cmbAbogados.getSelectedIndex();//dame el seleccionado
        if (pos != 0) {
            String dni;
            Usuario usuario = (Usuario) cmbAbogados.getSelectedItem();
            dni = usuario.getDni();
            cargarTablaCasos(dni);
            limpiarTablaAcciones();
            txtDescripcion.setText("");
        } else {
            try {
                limpiarTablaCasos();
                limpiarTablaAcciones();
                txtDescripcion.setText("");

            } catch (Exception ex) {

            }
        }
    }//GEN-LAST:event_cmbAbogadosActionPerformed

    private void tblCasosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCasosMouseClicked
        Caso seleccionado;
        String codCaso;
        int fila = tblCasos.rowAtPoint(evt.getPoint());
        // int columna = tabla.columnAtPoint(e.getPoint()); no me interesa la columna ha pulsado
        if ((fila > -1) /*&& (columna > -1)*/) {

            seleccionado = (Caso) modeloTablaCasos.getValueAt(fila, 0);//me devuelve el valor que exite en la fila que se ha hecho clic, columna 0
            codCaso = seleccionado.getCodigo();
            txtDescripcion.setText(seleccionado.getDescripcion());
            cargarTablaAcciones(codCaso);
        }
    }//GEN-LAST:event_tblCasosMouseClicked


