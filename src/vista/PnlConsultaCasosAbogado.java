    /**
     * carga la lista de abogados en el combo
     */
    private void cargarInfoCombo() {
        modeloCombo.addElement("Seleccione una opción");//sale como primera opcion la frase
        ArrayList<Usuario> usuarios = operaciones.todosUsuarios();
        modeloCombo.addAll(usuarios);

    }

    /**
     * carga los casos abiertos del abogado seleccionado
     * @param dni 
     */
    private void cargarTablaCasos(String dni) {
        limpiarTablaCasos();
        ArrayList<Caso> casos;
        casos = operaciones.buscarCasos(dni);
        for (Caso c : casos) {
            Vector v = new Vector();
            v.add(c);
            v.add(c.getTitulo());
            modeloTablaCasos.addRow(v);
        }
    }

    
    /**
     * carga las acciones del caso seleccionado
     * @param codCaso 
     */
    private void cargarTablaAcciones(String codCaso) {
        limpiarTablaAcciones();
        ArrayList<Accion> acciones = operaciones.buscarAcciones(codCaso);
        for (Accion a : acciones) {
            Vector v = new Vector();
            v.add(a.getCodAccion());
            v.add(a.getFecha());
            v.add(a.getDescripcion());
            modeloTablaAcciones.addRow(v);
        }
    }

    /**
     * borra los datos cargados en la tabla de casos
     */
    private void limpiarTablaCasos() {
        int nFilas = modeloTablaCasos.getRowCount();
        for (int pos = 0; pos < nFilas; pos++) {
            modeloTablaCasos.removeRow(0);
        }
    }

    /**
     * borra las acciones cargadas en la tabla de acciones
     */
    private void limpiarTablaAcciones() {
        int nFilas = modeloTablaAcciones.getRowCount();
        for (int pos = 0; pos < nFilas; pos++) {
            modeloTablaAcciones.removeRow(0);
        }
    }

