    private void btnCerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCerrarActionPerformed
        int pos = cmbCasos.getSelectedIndex();
        String codigo;
        Caso caso = (Caso) cmbCasos.getSelectedItem();
        codigo = caso.getCodigo();
        operaciones.cerrarCaso(codigo);
        JOptionPane.showMessageDialog(this, "Caso cerrado", "Información", JOptionPane.INFORMATION_MESSAGE);
        String codAccion = "CER";
        String fecha = Fecha.fechaActualNumero();
        String descripcionAccion = "Cierre del caso";
        operaciones.crearAccion(codigo, codAccion, fecha, descripcionAccion);
        limpiar();

        cmbCasos.removeItemAt(pos);

    }//GEN-LAST:event_btnCerrarActionPerformed


