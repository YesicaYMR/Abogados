    private void cmbCasosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbCasosActionPerformed
        int pos = cmbCasos.getSelectedIndex();//dame el seleccionado
        if (pos != 0) {
            //limpiarTablaAcciones();            
            String codigo;
            Caso caso = (Caso) cmbCasos.getSelectedItem();
            codigo = caso.getCodigo();
            cargarTablaAcciones(codigo);
            txtDescripcion.setText(caso.getDescripcion());
            lblDni.setText(caso.getDni());
            lblTitulo.setText(caso.getTitulo());
            btnCerrar.setEnabled(true);
        } else {
            btnCerrar.setEnabled(false);
            try {
                limpiar();
            } catch (Exception ex) {

            }
        }
    }//GEN-LAST:event_cmbCasosActionPerformed

